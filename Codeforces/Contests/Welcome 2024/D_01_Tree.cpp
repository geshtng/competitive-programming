#include <bits/stdc++.h>
using namespace std;

typedef long long LL;

void solve() {
    LL n; cin >> n;
    vector <LL> vec;

    for (int i = 0; i < n; i++) cin >> vec[i];
}

int main() {
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);

    int t; cin >> t;
    while (t--) solve();

    return 0;
}
